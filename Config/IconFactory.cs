        private static Bitmap CreateLineModeIcon(bool curve, bool ortho)
        {
            Bitmap bmp = new Bitmap(Size, Size);
            using (Graphics g = Graphics.FromImage(bmp))
            {
                g.SmoothingMode = SmoothingMode.AntiAlias;

                Color lineColor = AccentTeal;
                using (Pen pen = new Pen(lineColor, 2f))
                {
                    pen.StartCap = LineCap.Round;
                    pen.EndCap = LineCap.Round;

                    if (curve)
                    {
                        g.DrawBezier(pen, 3, 18, 5, 3, 19, 3, 21, 18);
                    }
                    else if (ortho)
                    {
                        g.DrawLines(pen, new PointF[] {
                            new PointF(3, 18), new PointF(3, 10),
                            new PointF(12, 10), new PointF(12, 18)
                        });
                    }
                    else
                    {
                        g.DrawLine(pen, 3, 18, 21, 6);
                    }
                }

                // 端点装饰
                using (SolidBrush dot = new SolidBrush(Color.FromArgb(100, 200, 210)))
                {
                    if (ortho)
                    {
                        g.FillEllipse(dot, 1, 16, 4, 4);
                        g.FillEllipse(dot, 10, 16, 4, 4);
                    }
                    else
                    {
                        g.FillEllipse(dot, 1, 16, 4, 4);
                        g.FillEllipse(dot, 19, 4, 4, 4);
                    }
                }
            }
            return bmp;
        }

